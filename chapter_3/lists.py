
#List
names = ["Lucas", "Rafael", "Daniel"]
print(names)


## Pop method
motorcycles = ["Honda", "Yamaha", "Suzuki"]
last = motorcycles.pop()
print(last)
print(motorcycles)


## Appending method
animals = ["Dog", "Cat", "Frog"]
print(animals)

animals.append("Monkey")
print(animals)


## Insert method
letters = ["A", "C", "D"]
print(letters)

letters.insert(1, "B")
print(letters)

letters.insert(4, "E")
print(letters)


## Modifying values
numbers = [10, 20, 40]
print(numbers)

numbers[2] = 30
print(numbers)


## Removing method
colors = ["Red", "Gold", "Black"]
print(colors)

colors.remove("Black")
print(colors)

## del statement
names = ["Lucas", "Xiao", "Someone"]
print(names)

del names[2]
print(names)
