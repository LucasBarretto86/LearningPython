# Basic Math Operators

## SUM
print("2 + 2 = 4")
print(2 + 2)

## DIVISION
print("2 / 2 = 1")
print(2 / 2)

## MODULE
print("5 % 4 = 1")
print(5 % 4)

## MULTIPLICATION
print("2 * 2 = 4")
print(2 * 2)

## SUBTRACTION
print("2 - 2 = 0")
print(2 - 2)

## POWER
print("3 ** 2 = 9")
print(3 ** 2)

## EQUATION
print("(3 + 5) * 4 = 32")
print((3  + 5) * 4)

print("3 + 5 * 4 = 23")
print(3 + 5 * 4)

## LONG NUMBERS
print("\nUniverse age")
universe_age = 14_000_000_000
print(universe_age)
